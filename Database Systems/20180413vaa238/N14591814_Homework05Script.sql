CREATE TABLE TEMP0 AS
SELECT DISTINCT INCLUDES.PROJECT
FROM INCLUDES, PROJECT
WHERE PROJECT.NAME = INCLUDES.PROJECT
GROUP BY INCLUDES.PROJECT
HAVING COUNT(INCLUDES.PROJECT) >= 2
MINUS
SELECT DISTINCT PROJECT.NAME
FROM PROJECT, FUNDING
WHERE FUNDING.PROJECT = PROJECT.NAME;

